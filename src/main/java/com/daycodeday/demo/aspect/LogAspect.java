package com.daycodeday.demo.aspect;

public class LogAspect {
    /**
     * 在调用一个方法之前，执行这个方法
     */
    public void before() {
        //这个方法中的逻辑，市是由我们自己写的
        System.out.println("Invoker Before Method");
    }

    /**
     * 在调用一个方法之后，执行这个方法
     */
    public void after() {
        System.out.println("Invoker After Method");
    }
}
